l1 = [1,19,4,8,44,15]
print(l1)

#sort method applied on l1
l1.sort()
print(l1)

#applying reverse method on l1
l1.reverse()
print(l1)

#applying append method on l1 , appends value at the end of the list
l1.append(88)
print(l1)

#applying insert method on l1, insert the value based on index
l1.insert(3,999)
print(l1)

#applying pop method on l1, pop removes the value based on index
l1.pop(2)
print(l1)

#applying remove method on l1, removes the value specified for removing
l1.remove(88)
print(l1)

